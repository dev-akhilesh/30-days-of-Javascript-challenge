// Activity 3: Comparison Operators

let num3 = 10;
let num4 = 5;

console.log('10 > 5:', num3 > num4);
console.log('10 < 5:', num3 < num4);
console.log('10 >= 5:', num3 >= num4);
console.log('10 <= 5:', num3 <= num4);


let num1 = 5;
let num2 = '5';

console.log('5 == "5":', num1 == num2);
console.log('5 === "5":', num1 === num2);
