// Comparison and Logical Operators Script

let x = 5;
let y = 10;

console.log('x == y:', x == y);
console.log('x === y:', x === y);
console.log('x != y:', x != y);
console.log('x !== y:', x !== y);
console.log('x < y:', x < y);
console.log('x > y:', x > y);
console.log('x <= y:', x <= y);
console.log('x >= y:', x >= y);

console.log('x < y && x > 0:', x < y && x > 0);
console.log('x < y || x > 10:', x < y || x > 10);
console.log('!(x < y):', !(x < y));
